async function getPrivateKeys() {
    try {
        const fileStream = fs.createReadStream("key.txt");
        const rl = readline.createInterface({
            input: fileStream,
            crlfDelay: Infinity
        });

        const privateKeys = [];
        for await (const line of rl) {
            const trimmedKey = line.trim();
            if (trimmedKey.length === 64 || trimmedKey.length === 66) {
                privateKeys.push(trimmedKey);
            }
        }
        return privateKeys;
    } catch (error) {
        console.error("Error reading key.txt:", error);
        process.exit(1);
    }
}

async function mintNFT(privateKey, attempt = 1) {
    try {
        const provider = new ethers.JsonRpcProvider(NETWORK_RPC, CHAIN_ID);
        const wallet = new ethers.Wallet(privateKey, provider);
        const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, wallet);

        console.log(`Minting NFT from wallet: ${wallet.address} (Attempt ${attempt})`);
        const gasLimit = 400000;
        const gasPrice = ethers.parseUnits("20", "gwei");
        const tx = await contract.safeMint({
            gasLimit: gasLimit,
            gasPrice: gasPrice
        });
        console.log(`Transaction sent for ${wallet.address}, waiting for confirmation...`);
        const receipt = await tx.wait();

        console.log(`✅ Transaction confirmed! TX Hash: ${receipt.hash}`);
        console.log(`🔗 Explorer Link: https://explorer-testnet.haust.app/tx/${receipt.hash}`);
    } catch (error) {
        console.error(`❌ Error minting for wallet ${privateKey.substring(0, 6)}...`, error.message);
        if (attempt < 3) {
            console.log(`🔄 Retrying for ${privateKey.substring(0, 6)} (Attempt ${attempt + 1})...`);
            return await mintNFT(privateKey, attempt + 1);
        } else {
            console.log(`🚫 Skipping ${privateKey.substring(0, 6)} after 3 failed attempts.`);
        }
    }
}
async function processKeys() {
    const privateKeys = await getPrivateKeys();
    console.log(`🔹 Found ${privateKeys.length} private keys. Processing with ${MAX_CONCURRENT} concurrent threads.`);

    let index = 0;
    while (index < privateKeys.length) {
        const batch = privateKeys.slice(index, index + MAX_CONCURRENT);
        await Promise.all(batch.map(key => mintNFT(key)));

        index += MAX_CONCURRENT;
    }
    console.log("🎉 All transactions processed!");
}
processKeys();
