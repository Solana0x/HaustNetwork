const MAX_CONCURRENT = 1;

async function getPrivateKeys() {
  try {
    const fileStream = fs.createReadStream("key.txt");
    const rl = readline.createInterface({
      input: fileStream,
      crlfDelay: Infinity
    });

    const privateKeys = [];
    for await (const line of rl) {
      const trimmedKey = line.trim();
      if (trimmedKey.length === 64 || trimmedKey.length === 66) {
        privateKeys.push(trimmedKey);
      }
    }
    return privateKeys;
  } catch (error) {
    console.error("Error reading key.txt:", error);
    process.exit(1);
  }
}

async function mintNFTs(privateKey, mintNFT1, attempt = 1) {
  try {
    const provider = new ethers.JsonRpcProvider(NETWORK_RPC, CHAIN_ID);
    const wallet = new ethers.Wallet(privateKey, provider);

    console.log(`\nMinting from wallet: ${wallet.address} (Attempt ${attempt})`);
    const gasLimit = 33269;
    const gasPrice = ethers.parseUnits("5", "gwei");
    if (mintNFT1) {
      const contract1 = new ethers.Contract(CONTRACT_ADDRESS_1, CONTRACT_ABI_1, wallet);
      console.log(`  1) Minting from FIRST NFT contract (${CONTRACT_ADDRESS_1})...`);
      const tx1 = await contract1.safeMint({ gasLimit, gasPrice });
      console.log("     Transaction sent. Waiting for confirmation (NFT1)...");
      const receipt1 = await tx1.wait();
      console.log(`     ✅ First NFT minted! TX Hash: ${receipt1.hash}`);
      console.log(`     🔗 Explorer Link: https://explorer-testnet.haust.app/tx/${receipt1.hash}`);
    } else {
      console.log("  Skipping NFT1 minting as per user choice.");
    }
    const contract2 = new ethers.Contract(CONTRACT_ADDRESS_2, CONTRACT_ABI_2, wallet);
    console.log(`  2) Minting from SECOND NFT contract (${CONTRACT_ADDRESS_2})...`);
    const tx2 = await contract2.safeMint({ gasLimit, gasPrice });
    console.log("     Transaction sent. Waiting for confirmation (NFT2)...");
    const receipt2 = await tx2.wait();
    console.log(`     ✅ Second NFT minted! TX Hash: ${receipt2.hash}`);
    console.log(`     🔗 Explorer Link: https://explorer-testnet.haust.app/tx/${receipt2.hash}`);

  } catch (error) {
    console.error(`❌ Error minting for wallet ${privateKey.substring(0, 6)}...`, error.message);
    if (attempt < 3) {
      console.log(`🔄 Retrying for ${privateKey.substring(0, 6)} (Attempt ${attempt + 1})...`);
      return await mintNFTs(privateKey, mintNFT1, attempt + 1);
    } else {
      console.log(`🚫 Skipping ${privateKey.substring(0, 6)} after 3 failed attempts.`);
    }
  }
}

async function processKeys(mintNFT1) {
  const privateKeys = await getPrivateKeys();
  console.log(`\n🔹 Found ${privateKeys.length} private keys. Processing with ${MAX_CONCURRENT} concurrent threads.`);

  let index = 0;
  while (index < privateKeys.length) {
    const batch = privateKeys.slice(index, index + MAX_CONCURRENT);
    await Promise.all(batch.map(key => mintNFTs(key, mintNFT1)));

    index += MAX_CONCURRENT;
  }
  console.log("\n🎉 All transactions processed!");
}

async function main() {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
  });

  rl.question("Do you want to mint NFT1 as well? (yes/no): ", async (answer) => {
    rl.close();

    const userInput = answer.trim().toLowerCase();
    const mintNFT1 = (userInput === "yes" || userInput === "y");
    await processKeys(mintNFT1);
    process.exit(0);
  });
}

